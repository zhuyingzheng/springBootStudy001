package sppringBootStudy001.com.model;

public class Menory {
    private Integer id;

    private String name;

    private Integer menory;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getMenory() {
        return menory;
    }

    public void setMenory(Integer menory) {
        this.menory = menory;
    }
}